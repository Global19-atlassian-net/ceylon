package org.eclipse.ceylon.common;

public class MiscUtil {

    private MiscUtil() {
    }

    public static int compare(String str1, String str2) {
        if (str1 == null && str2 == null) {
            return 0;
        }
        if (str1 == null) {
            return -1;
        }
        if (str2 == null) {
            return 1;
        }
        return str1.compareTo(str2);
    }
    
}
