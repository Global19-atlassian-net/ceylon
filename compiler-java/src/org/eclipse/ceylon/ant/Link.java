package org.eclipse.ceylon.ant;

import org.apache.tools.ant.types.DataType;
import org.apache.tools.ant.types.Reference;

public class Link extends DataType {
    public String url;
    public String pattern;

    @AntDoc("A URL or path of a module repository containing documentation "
            + "for external dependencies.")
    public void setUrl(String url){
        this.url = url;
    }
    
    @AntDoc("A module name prefix limiting the applicability of the "
            + "URL to modules whose name matches the prefix.")
    public void setPattern(String pattern){
        this.pattern = pattern;
    }
    
    @Override
    @AntDoc("A reference to a `<link>` defined outside this task.")
    public void setRefid(Reference reference) {
        super.setRefid(reference);
    }

    @Override
    public String toString() {
        if(pattern == null)
            return url;
        else
            return pattern+"="+url;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((url == null) ? 0 : url.hashCode());
        result = prime * result + ((pattern == null) ? 0 : pattern.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Link other = (Link) obj;
        if (url == null) {
            if (other.url != null)
                return false;
        } else if (!url.equals(other.url))
            return false;
        if (pattern == null) {
            if (other.pattern != null)
                return false;
        } else if (!pattern.equals(other.pattern))
            return false;
        return true;
    }
    
    
}
